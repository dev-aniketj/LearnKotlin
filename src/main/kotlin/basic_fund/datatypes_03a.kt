package basic_fund

fun main() {

//    INTEGER TYPES:
//    Byte(8-bit), Short(16-bit), Int(32-bit), Long(64-bit)
    val myByte: Byte = 23 //-128 to 127
    val myShort: Short = 25
    val myInt: Int = 27
    val myLong: Long = 24_234_342_653

//    FLOATING POINT NUMBER TYPES:
//    Float(32-bit), Double(64-bit)
    val myFloat: Float = 123.243f
    val myDouble: Double = 3.14986832827371

//    Printing all the DataTypes.
    println(myByte)
    println(myShort)
    println(myInt)
    println(myLong)
    println(myFloat)
    println(myDouble)

}